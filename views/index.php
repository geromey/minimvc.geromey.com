<h2>What is minimvc?</h2>

<p>minimvc is a minimalist mvc scruture which allows to build easily functional websites.</p>

<h2>Why was it made?</h2>

<p>Sometimes, in order to build a small website, you would like to avoid to use a relativelly big framework but still want to make some decent programming.</p>

<p>minimvc was made just for that.</p>

